var startQues = document.getElementById("startQues");
var flag = false;

function topic()
{
    for(var i = 0; i < selectQues.length; i++)
    {
        if(selectQues[i].checked)
        {
            var res = selectQues[i].value;
            selectQues[i].checked = false;
            flag = true;
        }
    }   
    if(flag){
        if(userName.value != "")
        {
            if(userName.value[0] != " ")
            {
                if(res === "html")
                {
                    startQues.href = "HTML.html";
                }
                else if(res === "css")
                {
                    startQues.href = "CSS.html";   
                }
                else if(res === "js")
                {
                    startQues.href = "JS.html"
                }
            }
            else
            {
                alert('First character must not be " SPACE" allowed');
            }
       
        }
        else
        {
            alert("Please Enter Your Name!");
        }
    }
    else
    {
        alert("Please Select the topic!")
    }
    
}